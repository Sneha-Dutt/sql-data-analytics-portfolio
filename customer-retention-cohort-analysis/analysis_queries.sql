# Customer Purchase Analysis

## Orders per Customer

```sql
SELECT c.customer_unique_id,
COUNT(o.order_id) AS orders_placed
FROM orders o
JOIN customers c
ON o.customer_id = c.customer_id
GROUP BY c.customer_unique_id
ORDER BY orders_placed DESC
LIMIT 10;
```

Identifies customers with the highest order counts.

---

## Average Orders per Customer

```sql
SELECT
ROUND(COUNT(o.order_id) / COUNT(DISTINCT c.customer_unique_id),2)
AS avg_orders_per_customer
FROM orders o
JOIN customers c
ON o.customer_id = c.customer_id;
```

Measures how frequently customers return to make purchases.

---

## Distribution of Customers by Order Count

```sql
WITH cte AS (
SELECT c.customer_unique_id,
COUNT(o.order_id) AS total_orders
FROM customers c
JOIN orders o
ON c.customer_id = o.customer_id
GROUP BY c.customer_unique_id
)

SELECT total_orders,
COUNT(*) AS number_of_customers
FROM cte
GROUP BY total_orders
ORDER BY total_orders;
```

Highlights customer retention patterns across order frequency.

---

## Repeat Customer Rate

```sql
WITH cte AS(
SELECT c.customer_unique_id,
COUNT(o.order_id) AS total_orders
FROM customers c
JOIN orders o
ON c.customer_id = o.customer_id
GROUP BY c.customer_unique_id
)

SELECT
SUM(CASE WHEN total_orders > 1 THEN 1 ELSE 0 END) * 100 / COUNT(*)
AS repeat_customer_rate
FROM cte;
```

Measures the percentage of customers who placed more than one order.

---

## Monthly New Customers

```sql
WITH cte AS (
SELECT c.customer_unique_id,
MIN(o.order_purchase_timestamp) AS first_purchase_date
FROM customers c
JOIN orders o
ON c.customer_id = o.customer_id
GROUP BY c.customer_unique_id
)

SELECT
DATE_FORMAT(first_purchase_date,'%Y-%m') AS month,
COUNT(DISTINCT customer_unique_id) AS new_customers
FROM cte
GROUP BY month
ORDER BY month;
```

Tracks customer acquisition growth over time.

