# Cohort Retention Analysis

## First Purchase per Customer (Cohort Identification)

```sql
WITH cte1 AS(
SELECT
c.customer_unique_id,
MIN(o.order_purchase_timestamp) AS first_purchase
FROM orders o
JOIN customers c
ON c.customer_id = o.customer_id
GROUP BY c.customer_unique_id
)

SELECT *
FROM cte1;
```

Determines when each customer entered the platform.

---

## Assign Customers to Cohorts

```sql
WITH cte1 AS (
SELECT
c.customer_unique_id,
MIN(o.order_purchase_timestamp) AS first_purchase
FROM orders o
JOIN customers c
ON c.customer_id = o.customer_id
GROUP BY c.customer_unique_id
)

SELECT
customer_unique_id,
DATE_FORMAT(first_purchase,'%Y-%m') AS cohort_month
FROM cte1;
```

---

## Cohort Retention Table

```sql
WITH cte1 AS(
SELECT c.customer_unique_id,
MIN(o.order_purchase_timestamp) AS first_order
FROM customers c
JOIN orders o
ON c.customer_id = o.customer_id
GROUP BY c.customer_unique_id
),

cte2 AS (
SELECT
c.customer_unique_id,
DATE_FORMAT(c1.first_order,'%Y-%m') AS cohort_month,
TIMESTAMPDIFF(
MONTH,
c1.first_order,
o.order_purchase_timestamp
) AS month_number
FROM customers c
JOIN orders o
ON c.customer_id = o.customer_id
JOIN cte1 c1
ON c1.customer_unique_id = c.customer_unique_id
)

SELECT
cohort_month,
month_number,
COUNT(DISTINCT customer_unique_id) AS retained_customers
FROM cte2
GROUP BY cohort_month, month_number
ORDER BY cohort_month, month_number;
```

Shows retention decline after the first purchase month.

