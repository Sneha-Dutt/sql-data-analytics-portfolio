# Customer Lifetime Value (CLTV)

## CLTV Table

```sql
SELECT
c.customer_unique_id,
COUNT(DISTINCT o.order_id) AS total_orders,
SUM(p.payment_value) AS lifetime_revenue,
ROUND(SUM(p.payment_value) /
COUNT(DISTINCT o.order_id),2) AS average_order_value
FROM customers c
JOIN orders o ON c.customer_id = o.customer_id
JOIN payments p ON p.order_id = o.order_id
GROUP BY c.customer_unique_id
ORDER BY lifetime_revenue DESC;
```

Highlights revenue generated by each customer over their lifetime.

---

## Average Customer Lifetime Value

```sql
WITH cte AS(
SELECT
c.customer_unique_id,
SUM(p.payment_value) AS revenue
FROM customers c
JOIN orders o
ON c.customer_id = o.customer_id
JOIN payments p
ON p.order_id = o.order_id
GROUP BY c.customer_unique_id
)

SELECT
ROUND(AVG(revenue),2) AS average_cltv
FROM cte;
```

