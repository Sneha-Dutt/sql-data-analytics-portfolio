# Exploratory Data Analysis (EDA)

## Total Orders

```sql
SELECT COUNT(*) AS total_orders
FROM orders;
```

Displays the overall transaction volume (~100K orders), confirming sufficient data for customer behavior analysis.

---

## Total Customers

```sql
SELECT COUNT(DISTINCT customer_unique_id)
FROM customers;
```

Shows the number of unique customers in the dataset.

---

## Orders by Status

```sql
SELECT order_status, COUNT(*) AS total_orders
FROM orders
GROUP BY order_status
ORDER BY total_orders DESC;
```

Helps identify operational performance such as delivered, cancelled, or unavailable orders.

---

## Monthly Orders Trend

```sql
SELECT
DATE_FORMAT(order_purchase_timestamp,'%Y-%m') AS order_month,
COUNT(*) AS number_of_orders
FROM orders
GROUP BY order_month
ORDER BY order_month;
```

Shows seasonal demand patterns, with order volume increasing toward the end of the year.

---

## Total Revenue

```sql
SELECT ROUND(SUM(payment_value),2) AS total_payments
FROM payments;
```

Provides an overview of total revenue generated on the platform.

